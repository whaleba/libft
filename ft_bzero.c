#include "libft.h"

void	ft_bzero(void *data, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)data;
	if (n > 0)
	{
		while (i < n)
		{
			str[i] = 0;
			i++;
		}
	}
}
